    <h3>Pilar 4: Polimorfismo</h3>
    <p>la polimorfismo es cuando varios objetos de diferentes clases, pero con una base común, se pueden usar de manera indistinta, sin tener que saber de qué clase exacta son para poder hacerlo.</p>
