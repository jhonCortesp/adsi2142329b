    <h2 id="pi">LOS CUATRO PILARES DE LA POO</h2>
    <div id="d4">
    <h3>Pilar 1: Encapsulación</h3>
    <p>La encapsulación es la característica de un lenguaje POO que permite que todo lo referente a un objeto quede aislado dentro de éste. Es decir, que todos los datos referentes a un objeto queden "encerrados" dentro de éste y sólo se puede acceder a ellos a través de los miembros que la clase proporcione (propiedades y métodos).</p>
    </div>
    <div id="d5">
    <h3>Pilar 2: Abstracción</h3>
    <p>la clase debe representar las características de la entidad hacia el mundo exterior, pero ocultando la complejidad que llevan aparejada</p>
    </div> 
